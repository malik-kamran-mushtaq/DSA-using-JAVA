/*
 * Swapping
 */
import java.util.*;
public class A59 {
    
    public static void main(String args[]){
        int a = 10;
        int b = 5;

        //swap
        int temp = a;
        a = b;
        b = temp;

        System.out.println("a= "+a);
        System.out.println("b= "+b);


    }
}
